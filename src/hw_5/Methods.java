package hw_5;

public class Methods {

    public int getSum(int a, int b) {
        return (a + b);
    }

    public int getDifference(int a, int b) {
        return a - b;
    }

    public int getMultiplication(int a, int b){
        return a * b;
    }

    public double getDivision(double a, double b) {
        return a / b;
    }
}
